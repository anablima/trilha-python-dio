# Este programa demonstra os tipos de dados básicos em Python:
# - Números inteiros (int)
# - Números de ponto flutuante (float)
# - Valores booleanos (bool)
# - Strings (str)

# Exemplo de números inteiros (int)
# Soma de inteiros: 11 + 10 + 1000 = 1021
print(11 + 10 + 1000)

# Exemplo de números de ponto flutuante (float)
# Soma de floats e inteiros (conversão automática para float): 1.5 + 1 + 0.5 = 3.0
print(1.5 + 1 + 0.5)

# Exemplos de valores booleanos (bool)
# True: representa verdadeiro
print(True)
# False: representa falso
print(False)

# Exemplo de string (str)
# String é um texto delimitado por aspas
print("Python")
