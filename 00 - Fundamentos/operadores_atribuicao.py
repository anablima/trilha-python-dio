saldo = 500
print(saldo)

# Atribuição simples
saldo = 200
print(saldo)

# Adição no valor já existente na variável
saldo += 10
print(saldo)

# Subtração no valor já existente na variável
saldo -= 5
print(saldo)

# Divisão inteiro com o valor já existente na variável
saldo //= 2
print(saldo)

# Dividindo o valor já existente na variável
saldo /= 2
print(saldo)

# Multiplicando o valor já existente na variável
saldo *= 10
print(saldo)

# Resto da divisão do valor já existente na variável
saldo %= 4
print(saldo)

# Elevando o valor já existente na variável
saldo **= 2
print(saldo)
